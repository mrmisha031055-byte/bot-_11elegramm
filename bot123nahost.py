def get_all_users_for_admin() -> list:
    """Получение всех пользователей для админ-панели"""
    conn = get_db()
    cur = conn.cursor()
    cur.execute("""
        SELECT user_id, username, first_name, current_day, completed_30, last_report_date 
        FROM users 
        ORDER BY current_day DESC, start_date DESC
    """)
    return cur.fetchall()

@async_wrapper
def reset_user_progress(user_id: int):
    """Полный сброс прогресса пользователя"""
    conn = get_db()
    cur = conn.cursor()
    
    cur.execute("DELETE FROM daily_reports WHERE user_id = ?", (user_id,))
    cur.execute("""
        UPDATE users 
        SET current_day = 1, 
            last_task_date = NULL, 
            last_report_date = NULL, 
            completed_30 = 0, 
            is_active = 1,
            start_date = ?
        WHERE user_id = ?
    """, (datetime.now().isoformat(), user_id))
    
    conn.commit()
    logger.info(f"Прогресс пользователя {user_id} полностью сброшен")

