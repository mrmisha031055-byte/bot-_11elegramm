                            day_tasks = DAILY_TASKS[current_day]
                            tasks_text = f"*{day_tasks['title']}*\n\n" + "\n\n".join(day_tasks["tasks"])
                            tasks_text += f"\n\n*Выполни задачи и нажми одну из кнопок:*"
                            
