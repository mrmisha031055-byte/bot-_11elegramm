@dp.message(Command("admin_sync"))
async def admin_sync_user(message: types.Message):
    if not is_admin(message.from_user.id):
        await message.answer("❌ У вас нет доступа к этой команде.")
        return
    
    args = message.text.split()
    if len(args) < 2:
        await message.answer("❌ *Использование:* `/admin_sync user_id`\nПример: `/admin_sync 123456789`", parse_mode="Markdown")
        return
    
    try:
        user_id = int(args[1])
        user = db_get_user(user_id)
        
        if not user:
            await message.answer(f"❌ Пользователь с ID {user_id} не найден.")
            return
        
        reports = db_get_user_reports(user_id)
        has_day30 = any(r[0] == 30 for r in reports)
        correct_completed = 1 if has_day30 else 0
        
        if reports:
            max_day = max(r[0] for r in reports)
            correct_day = max_day + 1 if max_day < 30 else 30
        else:
            correct_day = 1
        
        updates = []
        
        if user[8] != correct_completed:
            updates.append(f"completed_30: {user[8]} → {correct_completed}")
            with DB_LOCK:
                conn = get_db_connection()
                cur = conn.cursor()
                cur.execute("UPDATE users SET completed_30 = ? WHERE user_id = ?", (correct_completed, user_id))
                conn.commit()
                conn.close()
        
        if user[4] != correct_day:
            updates.append(f"current_day: {user[4]} → {correct_day}")
            with DB_LOCK:
                conn = get_db_connection()
                cur = conn.cursor()
                cur.execute("UPDATE users SET current_day = ? WHERE user_id = ?", (correct_day, user_id))
                conn.commit()
                conn.close()
        
        if updates:
            await message.answer(f"✅ *Синхронизация выполнена*\n\n👤 ID: {user_id}\n📊 Всего отчетов: {len(reports)}\n🔧 *Исправлено:*\n" + "\n".join(updates), parse_mode="Markdown")
            
            try:
                await bot.send_message(user_id, "🔄 *Ваши данные в марафоне были синхронизированы!*\n\nНажмите /start для продолжения.", parse_mode="Markdown")
            except:
                pass
        else:
            await message.answer(f"✅ *Синхронизация выполнена*\n\n👤 ID: {user_id}\n✨ *Данные уже корректны. Исправлений не требуется.*", parse_mode="Markdown")
        
    except ValueError:
        await message.answer("❌ Неверный ID пользователя.")
    except Exception as e:
        await message.answer(f"❌ Произошла ошибка: {str(e)}")

@dp.message(Command("stats"))
async def stats_command(message: types.Message):
    if not is_admin(message.from_user.id):
        return
    
    users = db_get_all_users()
    
    if not users:
        await message.answer("Нет данных")
        return
    
    active = [u for u in users if u[4] != 30 and u[4] == 0]
    completed = [u for u in users if u[4] == 1]
    
    day_counts = {}
    for user in active:
        day = user[3]
        day_counts[day] = day_counts.get(day, 0) + 1
    
    text = "📈 *Расширенная статистика*\n\n"
    text += f"👥 Всего пользователей: {len(users)}\n"
    text += f"✅ Активных: {len(active)}\n"
    text += f"🏆 Завершили: {len(completed)}\n\n"
    text += "*Распределение по дням:*\n"
    
    for day in sorted(day_counts.keys()):
        text += f"День {day}: {day_counts[day]} пользователей\n"
    
    await message.answer(text, parse_mode="Markdown")

