    callback_data = callback.data
    
    parts = callback_data.split('_')
    if len(parts) < 3:
        await callback.answer("❌ Ошибка формата", show_alert=True)
        return
    
    report_key = parts[1]
    reported_day = int(parts[2])
    
    user_data = db_get_user(user_id)
